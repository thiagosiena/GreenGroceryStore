    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed

        String email = codeTextField.getText();
        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a email", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            
            User u = new UserDAO().getUserByEmail(email);
            if (u != null) {
                resultTextArea.setText("User found:\n\n");
                resultTextArea.append("User Code: " + u.getId() + "\n");
                resultTextArea.append("Name: " + u.getName() + "\n");
                resultTextArea.append("Email: " + u.getEmail() + "\n");
                resultTextArea.append("Password: " + u.getPassword() + "\n");
                
            } else {
                resultTextArea.setText("No user found with this email: " + email);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Email", "ERROR", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR" + e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_searchButtonActionPerformed

